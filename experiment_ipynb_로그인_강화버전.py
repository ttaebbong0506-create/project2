def login(username, password):
    if not username or not password:
        return False, None, "아이디와 비밀번호를 입력하세요."
    username = username.strip()

    now = time.time()
    rec = login_attempts.get(username)
    if rec and rec.get("locked_until", 0) > now:       # 잠금 상태면 차단
        wait = int(rec["locked_until"] - now)
        return False, None, f"로그인 시도가 너무 많습니다. {wait}초 후 다시 시도하세요."

    user = users.get(username)
    ok = False
    if user is not None:
        candidate = _hash_password(password, user["salt"])
        ok = hmac.compare_digest(candidate, user["hash"])  # 타이밍 공격 방어 비교

    if ok:
        login_attempts.pop(username, None)             # 실패 기록 초기화
        token = secrets.token_hex(16)                  # 세션 토큰 발급
        sessions[token] = {"username": username, "login_time": now}
        return True, token, f"{username}님 로그인 성공!"

    rec = login_attempts.setdefault(username, {"fails": 0, "locked_until": 0})
    rec["fails"] += 1
    if rec["fails"] >= MAX_FAILED_ATTEMPTS:            # 5회 실패 시 잠금
        rec["locked_until"] = now + LOCKOUT_SECONDS
        rec["fails"] = 0
        return False, None, f"실패 횟수 초과. 계정이 {LOCKOUT_SECONDS//60}분간 잠겼습니다."
    remain = MAX_FAILED_ATTEMPTS - rec["fails"]
    return False, None, f"아이디 또는 비밀번호가 틀렸습니다. (남은 시도 {remain}회)"


