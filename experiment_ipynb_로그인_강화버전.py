def load_restaurant_data(file_path):
    df = pd.read_excel(file_path)

    if "카데고리" in df.columns and "카테고리" not in df.columns:
        df = df.rename(columns={"카데고리": "카테고리"})

    df = df[["식당명", "카테고리", "메뉴", "가격", "제휴", "평점"]].copy()

    for col in ["식당명", "카테고리", "제휴", "평점"]:
        df[col] = df[col].ffill()

    df = df.dropna(subset=["메뉴", "가격"])
    df["가격"] = pd.to_numeric(df["가격"], errors="coerce")
    df["평점"] = pd.to_numeric(df["평점"], errors="coerce")
    df = df.dropna(subset=["가격"])
    df = df[df["가격"] > 0]

    for col in ["식당명", "카테고리", "메뉴", "제휴"]:
        df[col] = df[col].astype(str).str.strip()

    return df.reset_index(drop=True)


def contains_text(target, keyword):
    if not keyword:
        return False
    if pd.isna(target):
        return False
    return keyword.lower() in str(target).lower()


def is_partner(affiliate_text, college_name):
    return contains_text(affiliate_text, college_name)


def row_to_meal_dict(row, college_name=None):
    return {
        "식당명": str(row["식당명"]),
        "카테고리": str(row["카테고리"]),
        "메뉴": str(row["메뉴"]),
        "가격": int(row["가격"]),
        "제휴": str(row["제휴"]),
        "평점": float(row["평점"]) if pd.notna(row["평점"]) else None,
        "제휴식당여부": is_partner(row["제휴"], college_name),
    }


def meal_to_text(meal):
    partner_text = " [제휴]" if meal.get("제휴식당여부", False) else ""
    rating_text = f"평점 {meal['평점']:.1f}" if meal.get("평점") is not None else "평점 없음"
    return f"{meal['식당명']} - {meal['메뉴']}\n{meal['가격']:,}원 / {rating_text} / {meal['카테고리']}{partner_text}"


def calculate_total_cost(plan):
    return sum(day["점심"]["가격"] + day["저녁"]["가격"] for day in plan)


def make_score(df, college_name=None, priority="평점", preferred_category=None):
    df = df.copy()

    avg_price = df["가격"].mean()
    avg_rating = df["평점"].mean()
    df["평점"] = df["평점"].fillna(avg_rating)

    df["가격점수"] = (avg_price / df["가격"]).clip(upper=2.5)
    df["제휴점수"] = df["제휴"].apply(lambda x: 1 if is_partner(x, college_name) else 0)
    df["카테고리점수"] = df["카테고리"].apply(
        lambda x: 1 if contains_text(x, preferred_category) else 0
    )

    if priority == "평점":
        wr, wp, wc, wprice = 0.65, 0.15, 0.10, 0.10
    elif priority == "제휴":
        wr, wp, wc, wprice = 0.35, 0.40, 0.15, 0.10
    elif priority == "카테고리":
        wr, wp, wc, wprice = 0.30, 0.15, 0.45, 0.10
    else:
        wr, wp, wc, wprice = 0.50, 0.20, 0.20, 0.10

    df["추천점수"] = (
        df["평점"] * wr
        + df["제휴점수"] * wp * 5
        + df["카테고리점수"] * wc * 5
        + df["가격점수"] * wprice
    )

    return df


def select_meal_plan(df, total_budget, college_name=None, priority="평점", preferred_category=None, days=7):
    df = make_score(df, college_name, priority, preferred_category)

    total_meals = days * 2
    budget_per_meal = total_budget / total_meals

    candidates = df[df["가격"] <= budget_per_meal * 1.4].copy()
    if len(candidates) < total_meals:
        candidates = df.copy()

    candidates = candidates.sort_values(
        by=["추천점수", "평점", "가격"],
        ascending=[False, False, True]
    ).reset_index(drop=True)

    selected = []
    used_menus = set()
    restaurant_counts = {}

    for _, row in candidates.iterrows():
        restaurant = row["식당명"]
        menu = row["메뉴"]

        if menu in used_menus:
            continue
        if restaurant_counts.get(restaurant, 0) >= 2:
            continue

        meal = row_to_meal_dict(row, college_name)
        selected.append(meal)

        used_menus.add(menu)
        restaurant_counts[restaurant] = restaurant_counts.get(restaurant, 0) + 1

        if len(selected) == total_meals:
            break

    if len(selected) < total_meals:
        return None, "식당 데이터가 부족합니다."

    if sum(x["가격"] for x in selected) > total_budget:
        min_possible = df.nsmallest(total_meals, "가격")["가격"].sum()
        return None, f"예산 부족입니다. 최소 필요 예산은 약 {int(min_possible):,}원입니다."

    random.shuffle(selected)

    plan = []
    idx = 0

    for day in DAY_NAMES[:days]:
        plan.append({
            "요일": day,
            "점심": selected[idx],
            "저녁": selected[idx + 1]
        })
        idx += 2

    return plan, calculate_total_cost(plan)


