def situation_recommend(situation, college_name):
    try:
        df = load_restaurant_data(FILE_PATH)
    except FileNotFoundError:
        return "엑셀 파일을 찾을 수 없습니다. 코랩 왼쪽 파일창에 엑셀 파일을 업로드하세요."
    except Exception as e:
        return f"오류 발생: {e}"

    preset = SITUATION_PRESETS[situation]
    filtered = df.copy()

    if preset.get("max_price"):
        filtered = filtered[filtered["가격"] <= preset["max_price"]]

    if preset.get("min_price"):
        filtered = filtered[filtered["가격"] >= preset["min_price"]]

    if preset.get("categories"):
        filtered = filtered[
            filtered["카테고리"].apply(
                lambda x: any(cat in str(x) for cat in preset["categories"])
            )
        ]

    if situation == "🍜 혼밥 / 가성비":
        college = None
    else:
        college = college_name.strip() if college_name else None

    if preset.get("partner_only"):
        if not college:
            return "제휴 할인 맛집을 보려면 단과대를 입력해야 합니다."

        filtered = filtered[
            filtered["제휴"].apply(lambda x: is_partner(x, college))
        ]

    if len(filtered) == 0:
        return f"'{situation}' 조건에 맞는 식당이 없습니다."

    filtered["평점"] = filtered["평점"].fillna(filtered["평점"].mean())

    if preset["sort_by"] == "가격":
        filtered = filtered.sort_values(
            by=["가격", "평점"],
            ascending=[True, False]
        )
    else:
        filtered = filtered.sort_values(
            by=["평점", "가격"],
            ascending=[False, True]
        )

    results = []
    seen = set()

    for _, row in filtered.iterrows():
        key = (row["식당명"], row["메뉴"])

        if key in seen:
            continue

        seen.add(key)

        partner = " 🏷️제휴" if college and is_partner(row["제휴"], college) else ""

        results.append(
            f"**{len(results) + 1}. {row['식당명']}** - {row['메뉴']}  \n"
            f"💰 {int(row['가격']):,}원 | ⭐ {row['평점']:.1f} | 🍽️ {row['카테고리']}{partner}"
        )

        if len(results) >= preset["top_n"]:
            break

    if len(results) == 0:
        return f"'{situation}' 조건에 맞는 추천 결과가 없습니다."

    return (
        f"### {situation}\n"
        f"{preset['description']}\n\n"
        + "\n\n".join(results)
    )


