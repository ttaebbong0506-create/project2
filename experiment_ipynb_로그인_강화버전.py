def get_restaurant_counts(plan, exclude_day=None, exclude_meal_type=None):
    counts = {}

    for day in plan:
        for meal_type in MEAL_TYPES:
            if day["요일"] == exclude_day and meal_type == exclude_meal_type:
                continue

            restaurant = day[meal_type]["식당명"]
            counts[restaurant] = counts.get(restaurant, 0) + 1

    return counts


def get_used_menu_names(plan, exclude_day=None, exclude_meal_type=None):
    used = set()

    for day in plan:
        for meal_type in MEAL_TYPES:
            if day["요일"] == exclude_day and meal_type == exclude_meal_type:
                continue

            used.add(day[meal_type]["메뉴"])

    return used


def build_replacement_candidates(
    df,
    plan,
    total_budget,
    college_name,
    priority,
    preferred_category,
    day_name,
    meal_type,
    top_n=3
):
    df = make_score(df, college_name, priority, preferred_category)

    target_day = None

    for day in plan:
        if day["요일"] == day_name:
            target_day = day
            break

    if target_day is None:
        return [], "해당 요일을 찾을 수 없습니다."

    old_meal = target_day[meal_type]
    current_total = calculate_total_cost(plan)
    remaining_budget = total_budget - (current_total - old_meal["가격"])

    used_menus = get_used_menu_names(plan, day_name, meal_type)
    restaurant_counts = get_restaurant_counts(plan, day_name, meal_type)

    candidates = df.copy()
    candidates = candidates[candidates["메뉴"] != old_meal["메뉴"]]
    candidates = candidates[candidates["가격"] <= remaining_budget]
    candidates = candidates[candidates["식당명"].apply(lambda x: restaurant_counts.get(x, 0) < 2)]

    if len(candidates) == 0:
        return [], "예산 또는 식당 2회 제한 때문에 바꿀 수 있는 메뉴가 없습니다."

    candidates["중복메뉴패널티"] = candidates["메뉴"].apply(lambda x: 1 if x in used_menus else 0)

    candidates = candidates.sort_values(
        by=["중복메뉴패널티", "추천점수", "평점", "가격"],
        ascending=[True, False, False, True]
    ).reset_index(drop=True)

    result = []
    seen = set()

    for _, row in candidates.iterrows():
        key = (row["식당명"], row["메뉴"], int(row["가격"]))

        if key in seen:
            continue

        seen.add(key)
        result.append(row_to_meal_dict(row, college_name))

        if len(result) == top_n:
            break

    return result, None


def generate_plan(budget, college, priority, category):
    try:
        if budget is None:
            return make_error_image("예산을 입력하세요."), None, None, None, "예산을 입력하세요."

        budget = int(budget)
        college_name = college.strip() if college else None
        preferred_category = category.strip() if category else None

        df = load_restaurant_data(FILE_PATH)

        plan, result = select_meal_plan(
            df,
            budget,
            college_name=college_name,
            priority=priority,
            preferred_category=preferred_category
        )

        if plan is None:
            return make_error_image(result), None, None, None, result

        image_path = save_plan_as_image(plan, total_budget=budget)

        settings = {
            "budget": budget,
            "college_name": college_name,
            "priority": priority,
            "preferred_category": preferred_category
        }

        return image_path, plan, df, settings, "식단표 생성 완료!"

    except FileNotFoundError:
        msg = "엑셀 파일을 찾을 수 없습니다. 코랩 왼쪽 파일창에 엑셀 파일을 업로드하세요."
