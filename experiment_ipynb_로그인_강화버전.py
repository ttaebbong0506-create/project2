def learn_user_weights():
    if len(review_history) == 0:
        return {
            "rating_weight": 0.50,
            "price_weight": 0.20,
            "category_weight": 0.20,
            "partner_weight": 0.10,
            "best_category": None,
            "price_sensitivity": "보통"
        }

    df = pd.DataFrame(review_history)

    avg_price_score = df["가격만족도"].mean()
    avg_taste_score = df["맛만족도"].mean()

    category_score = df.groupby("카테고리")["평균만족도"].mean()
    best_category = category_score.sort_values(ascending=False).index[0]

    # 가격 만족도가 낮으면 가격에 민감한 사용자로 판단
    if avg_price_score <= 3:
        price_weight = 0.35
        price_sensitivity = "높음"
    elif avg_price_score >= 4.5:
        price_weight = 0.10
        price_sensitivity = "낮음"
    else:
        price_weight = 0.20
        price_sensitivity = "보통"

    # 맛 만족도가 높을수록 평점/맛 중심 추천
    if avg_taste_score >= 4:
        rating_weight = 0.45
    else:
        rating_weight = 0.30

    category_weight = 0.35
    partner_weight = 0.10

    total = rating_weight + price_weight + category_weight + partner_weight

    return {
        "rating_weight": rating_weight / total,
        "price_weight": price_weight / total,
        "category_weight": category_weight / total,
        "partner_weight": partner_weight / total,
        "best_category": best_category,
        "price_sensitivity": price_sensitivity
    }


def show_learned_weights():
    weights = learn_user_weights()

    if weights["best_category"] is None:
        return "아직 평가 기록이 없습니다. 먼저 식단 평가를 해주세요."

    return f"""
### 🧠 자동 학습된 추천 가중치

| 항목 | 반영 비율 |
|---|---:|
| 평점 중요도 | {weights["rating_weight"] * 100:.1f}% |
| 가격 중요도 | {weights["price_weight"] * 100:.1f}% |
| 카테고리 취향 | {weights["category_weight"] * 100:.1f}% |
| 제휴 여부 | {weights["partner_weight"] * 100:.1f}% |

### 분석 결과

- 가장 선호하는 카테고리: **{weights["best_category"]}**
- 가격 민감도: **{weights["price_sensitivity"]}**

> 사용자의 평가 기록을 바탕으로 추천 기준을 자동 조정했습니다.
"""

def make_score_by_learned_weights(df, college_name=None):
    weights = learn_user_weights()
    df = df.copy()

    avg_price = df["가격"].mean()
    avg_rating = df["평점"].mean()
    df["평점"] = df["평점"].fillna(avg_rating)

    best_category = weights["best_category"]

    df["가격점수"] = (avg_price / df["가격"]).clip(upper=2.5)
    df["제휴점수"] = df["제휴"].apply(lambda x: 1 if is_partner(x, college_name) else 0)
    df["카테고리점수"] = df["카테고리"].apply(
        lambda x: 1 if best_category and best_category in str(x) else 0
    )

    df["추천점수"] = (
        df["평점"] * weights["rating_weight"]
        + df["가격점수"] * weights["price_weight"]
        + df["카테고리점수"] * weights["category_weight"] * 5
        + df["제휴점수"] * weights["partner_weight"] * 5
    )

    return df


def generate_plan_with_auto_weights(budget, college):
    if len(review_history) == 0:
        return None, None, None, None, "아직 평가 기록이 없습니다. 먼저 식단 평가를 해주세요."

    try:
        budget = int(budget)
        college_name = college.strip() if college else None

        df = load_restaurant_data(FILE_PATH)
        df = make_score_by_learned_weights(df, college_name)

        total_meals = 14
        budget_per_meal = budget / total_meals

        candidates = df[df["가격"] <= budget_per_meal * 1.4].copy()

        if len(candidates) < total_meals:
            candidates = df.copy()

        candidates = candidates.sort_values(
            by=["추천점수", "평점", "가격"],
            ascending=[False, False, True]
        ).reset_index(drop=True)

        selected = []
        used_menus = set()
        restaurant_counts = {}

        for _, row in candidates.iterrows():
            restaurant = row["식당명"]
            menu = row["메뉴"]

            if menu in used_menus:
                continue

            if restaurant_counts.get(restaurant, 0) >= 2:
                continue

            meal = row_to_meal_dict(row, college_name)
            selected.append(meal)

            used_menus.add(menu)
            restaurant_counts[restaurant] = restaurant_counts.get(restaurant, 0) + 1

            if len(selected) == total_meals:
                break

        if len(selected) < total_meals:
            return None, None, None, None, "식당 데이터가 부족합니다."

        if sum(x["가격"] for x in selected) > budget:
            min_possible = df.nsmallest(total_meals, "가격")["가격"].sum()
            return None, None, None, None, f"예산 부족입니다. 최소 필요 예산은 약 {int(min_possible):,}원입니다."

        random.shuffle(selected)

        plan = []
        idx = 0

        for day in DAY_NAMES:
            plan.append({
                "요일": day,
                "점심": selected[idx],
                "저녁": selected[idx + 1]
            })
            idx += 2

        image_path = save_plan_as_image(plan, total_budget=budget)

        settings = {
            "budget": budget,
            "college_name": college_name,
            "priority": "자동 학습",
            "preferred_category": learn_user_weights()["best_category"]
        }

        save_usage_history(plan, settings)

        return image_path, plan, df, settings, "학습된 자동 가중치로 식단 추천 완료!"

    except Exception as e:
        return None, None, None, None, f"오류 발생: {e}"

