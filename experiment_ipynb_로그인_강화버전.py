def wrap_text_by_width(text, font, max_width):
    lines = []

    for paragraph in str(text).split("\n"):
        line = ""

        for char in paragraph:
            test = line + char
            bbox = font.getbbox(test)
            w = bbox[2] - bbox[0]

            if w <= max_width:
                line = test
            else:
                if line:
                    lines.append(line)
                line = char

        if line:
            lines.append(line)

    return lines


def draw_centered_text(draw, text, box, font, fill="black"):
    x1, y1, x2, y2 = box
    bbox = font.getbbox(str(text))
    w = bbox[2] - bbox[0]
    h = bbox[3] - bbox[1]

    x = x1 + (x2 - x1 - w) / 2
    y = y1 + (y2 - y1 - h) / 2

    draw.text((x, y), str(text), font=font, fill=fill)


def draw_wrapped_text(draw, text, box, font, fill="black"):
    x1, y1, x2, y2 = box
    padding = 14
    max_width = x2 - x1 - padding * 2

    lines = wrap_text_by_width(text, font, max_width)
    line_height = int(font.size * 1.35)
    total_height = len(lines) * line_height

    y = y1 + (y2 - y1 - total_height) / 2

    for line in lines:
        draw.text((x1 + padding, y), line, font=font, fill=fill)
        y += line_height


